i = 3
while i != 0:
    print ("meow")
    i = i - 1


# i is initialized to 3, then the while loop is triggered checking if i is not equal to 0 and if the statement is true it prints meow and re-initializes i as its current value minus 1. Eventually it will check if i is not equal to zero and i will be equal to zero rendering the statement false and stoppingthe program
























